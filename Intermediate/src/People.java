
public class People {
	

		String name;
		int age;
		String jobTitle;
		
		
		public People (String pName, int pAge, String pJobTitle) {
	
		this.name = pName;
		this.age = pAge;
		this.jobTitle = pJobTitle;
		}
		
	    public String getName() {
	    return name;
	    }
	    public int getAge() {
	    return age;
	    }
	    public String getJobTitle() {
	    return jobTitle;	
	    }
}
